// 自定义命令：等待加载结束
Cypress.Commands.add('waitForLoading', () => {
  cy.get('.loading-indicator').should('not.exist');
});

// 自定义命令：检查页面标题
Cypress.Commands.add('shouldHaveTitle', (title) => {
  cy.title().should('include', title);
}); 